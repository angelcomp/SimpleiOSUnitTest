import Foundation

class Arithmetic {
    func add(_ num1: Int, _ num2: Int) -> Int {
        return num1 + num2
    }
    
    func sub(_ num1: Int, _ num2: Int) -> Int {
        return num1 - num2
    }
    
    func mul(_ num1: Int, _ num2: Int) -> Int {
        return num1 * num2
    }
    
    func div(_ num1: Int, _ num2: Int) -> Int {
        return num1 / num2
    }
}
